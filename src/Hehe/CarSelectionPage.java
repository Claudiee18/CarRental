    public void saveRentalDetails(String carName, String make, String model, int year, double price, int totalDays) {
    String query = "INSERT INTO rental_details (car_name, make, model, year, price, total_days, total_cost, is_paid) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";

    try (Connection conn = DatabaseConnector.getConnection();
         PreparedStatement pstmt = conn.prepareStatement(query)) {

        pstmt.setString(1, carName);
        pstmt.setString(2, make);
        pstmt.setString(3, model);
        pstmt.setInt(4, year);
        pstmt.setDouble(5, price);
        pstmt.setInt(6, totalDays);
        pstmt.setDouble(7, price * totalDays);
        pstmt.setBoolean(8, false); // Initially unpaid

        pstmt.executeUpdate();
        System.out.println("Rental details saved successfully!");
    } catch (SQLException e) {
        e.printStackTrace();
    }
}
    
    
    
