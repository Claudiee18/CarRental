    public double fetchCarPrice(String carName, String make, String model, int year) {
    String query = "SELECT Car_Price FROM carselection WHERE car_name = ? AND Make = ? AND Model = ? AND Year = ?";
    try (PreparedStatement pstmt = connection.prepareStatement(query)) {
        pstmt.setString(1, carName);
        pstmt.setString(2, make);
        pstmt.setString(3, model);
        pstmt.setInt(4, year);
        try (ResultSet rs = pstmt.executeQuery()) {
            if (rs.next()) {
                return rs.getDouble("Car_Price");
            }
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
    return 0; // Return 0 if no price is found
}
    
